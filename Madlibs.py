#Creating a story using Adjectives, Verbs and Nouns.

adjective1 = input("Enter an adjective (description): ")
noun1 = input("Enter a noun (person, place or thing): ")
verb1 = input("Enter a verb ending in 'ing' (action): ")
adjective2 = input("Enter a 2nd adjective (description): ")
noun2 = input("Enter a 2nd noun (person, place or thing): ")
verb2 = input("Enter a 2nd verb ending in 'ing' (action): ")

print("This is my story.")
print(f"I visited a {adjective1} theme park.")
print(f"There i saw a {noun1} in a {noun2}.")
